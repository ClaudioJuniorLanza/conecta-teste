	Web Beacons<br>
	Um Web beacon é uma imagem eletrônica, também chamada single-pixel (1 x 1) ou 
	pixel transparente, que é colocada em código em uma página da internet. Um 
	Webbeacon tem finalidades similares aos Cookies. Adicionalmente um Webbeacon é 
	utilizado para medir padrões de tráfego dos Usuários de uma página a outra com 
	objetivo de maximizar o fluxo de tráfego através da internet. O Usuário e o 
	visitante do site do Conecta Sementes manifesta conhecer e aceitar que o Conecta 
	Sementes poderá usar um sistema de coleta de dados mediante a utilização de Web 
	beacons.<br>
