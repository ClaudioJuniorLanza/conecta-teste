	Cookies<br>
	O Usuário e o visitante do site do Conecta Sementes manifesta conhecer e aceitar 
	que poderá ser utilizado um sistema de coleta de dados de navegação mediante à 
	utilização de cookies. Os cookies são pequenos arquivos que se instalam no disco 
	rígido, com uma duração limitada de tempo que ajudam a personalizar os serviços. 
	Também serão ofertadas algumas funcionalidades que só estarão disponíveis pelo 
	emprego dos cookies. Os cookies são utilizados com o fim de conhecer os 
	interesses, o comportamento e a demografia de quem visita ou é Usuário do site, 
	e dessa forma, compreender melhor suas necessidades e interesses e dar-lhes um 
	melhor serviço ou prover-lhes informação relacionada. Também será usada a 
	informação obtida por intermédio dos cookies para analisar as páginas navegadas 
	pelo visitante ou Usuário, as buscas realizadas, melhorar as iniciativas 
	comerciais e promocionais, mostrar publicidade ou promoções, banners de 
	interesse, notícias sobre o Conecta Sementes, aperfeiçoar a oferta de conteúdos 
	e bens, personalizar esses conteúdos, apresentação e serviços; também poderá 
	haver utilização de cookies para promover e fazer cumprir as regras e segurança 
	do site. O Conecta Sementes poderá adicionar cookies nos e-mails que envia para 
	medir a efetividade das promoções. Utilizam-se adicionalmente os cookies para 
	que o Usuário não tenha que introduzir sua senha tão frequentemente durante uma 
	seção de navegação, também para contabilizar e corroborar os registros e a 
	atividade do Usuário relacionados a outros acordos comerciais, sempre tendo como 
	objetivo da instalação dos cookies o benefício do Usuário. O Conecta Sementes 
	não usará os cookies com fins alheios à sua operação.<br>
	A instalação, permanência e existência dos cookies no computador do Usuário 
	dependerão de sua exclusiva vontade e poderão ser eliminados de seu computador 
	quando o Usuário assim o deseje. Para saber como retirar os cookies do sistema é 
	necessário revisar a seção Contato do navegador. Adicionalmente, podem ser 
	encontrados cookies ou outros sistemas similares instalados por terceiros em 
	certas páginas do site do Conecta Sementes. Por exemplo, ao navegar por uma 
	página criada por um Usuário pode ser que exista um cookie embutido ali.<br>
	Por fim, o Conecta Sementes armazena cookies para oferecer uma experiência mais 
	interativa no site, com base nas ações do usuário. O usuário pode, a qualquer 
	momento, excluir os cookies armazenados, configurar o navegador para solicitar 
	aprovação antes de armazenar cookies ou diretamente impedir que os cookies sejam 
	armazenados.<br>
	Este procedimento é feito, em cada navegador, de forma diferente e é necessário 
	realizá-lo em cada um dos navegadores utilizados. Abaixo, oferecemos os 
	diferentes procedimentos para gerenciar essa configuração nos navegadores mais 
	usados:<br>
	- Microsoft Windows Explorer: 
	https://support.microsoft.com/pt-br/help/17442/windows-internet-explorer-delete-manage-cookies<br>
