	- Mozilla Firefox: 
	https://support.mozilla.org/pt-BR/kb/ative-e-desative-os-cookies-que-os-Site-usam<br>
	- Apple Safari: 
	https://support.apple.com/kb/PH21411?locale=pt_BR&amp;viewlocale=pt_BR<br>
